//Цикли в JavaScript
//Цикли — це інструкції, які дозволяють повторювати виконання певного блоку коду кілька разів, поки виконується певна умова. Вони корисні, коли потрібно виконати одну і ту ж дію багаторазово, наприклад, для проходження по масиву або виконання повторних обчислень.

//1. Цикл for
//Цикл for використовується, коли заздалегідь відома кількість повторень, або коли є чітка структура, як наприклад, робота з індексами.

for (let i = 0; i < 5; i++) {
  console.log(i);
}
//Цей цикл виведе числа від 0 до 4. На кожній ітерації значення i збільшується на 1, доки умова i < 5 залишається істинною.

//2. Цикл while
//Цикл while виконує блок коду доти, поки умова залишається істинною.
//Цей цикл більше підходить для ситуацій, коли кількість ітерацій заздалегідь невідома, але потрібно виконувати дії до певного моменту.

let i = 0;
while (i < 5) {
  console.log(i);
  i++;
}
//Цей цикл працює так само, як і попередній, але умова перевіряється на початку кожної ітерації.
//Як тільки i стане більше або дорівнює 5, цикл зупиниться.
